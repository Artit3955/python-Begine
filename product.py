product = {'ดินสอกดpentel':{'ราคา':50,'สี':'แดง'},
           'ยางลบ':{'ราคา':3,'สี':'เขียว'},
           'ดินสอสี':{'ราคา':100,'สี':'ส้ม'}}

while True:
    print('-----กรุณากรอกข้อมูลสินค้า----')
    p = input('ชื่อสินค้า: ')
    print('------')
    if p in product:
        print(f'สินค้า: {product[p]} ราคา: {product[p]['ราคา']} สี: {product[p]['สี']}')
    else:
        print('ไม่มีสินค้าในระบบ')
